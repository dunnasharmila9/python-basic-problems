## 16.Compare two numbers using ==, !=, >, <, >=, and <= operators.
num1 = float(input("Enter first number: "))
num2 = float(input("Enter second number: "))
print("Is equal to (==):", num1 == num2)
print("Is not equal to (!=):", num1 != num2)
print("Is greater than (>):", num1 > num2)
print("Is less than (<):", num1 < num2)
print("Is greater than or equal to (>=):", num1 >= num2)
print("Is less than or equal to (<=):", num1 <= num2)